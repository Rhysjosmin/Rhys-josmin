function createRecord(com,fn){
    
    let line = CodeLine.cloneNode(true) 
    line.textContent=">>"+com 
    terminal.appendChild(line)
    line = CodeLine.cloneNode(true)
    if(fn=="error"){

        errNum++
        if(errNum==2){
            line.textContent=sl+"Is Your keyboard working?"  
        }else if(errNum==3){
            line.textContent=sl+"Seriously Are You Even trying?"  
        }else if(errNum==4){
            line.textContent=sl+"Bruh" 
        }else if(errNum==5){
            line.textContent=sl+"You're getting on my nerves" 
        }else if(errNum==69){
            line.textContent=sl+"You've typed wrong an Nice number of times ^_+ " 
        }else{
        line.textContent=sl+"No Dummy, \""+com +"\" Is Not Recognised As A Valid Command"  
        }
        
    }
    else if(fn=="list"){
        List()
    }else if(fn=="print"){
        line.textContent=sl+com 
    }else if(fn=="empty"){
        line.textContent=sl
    }else if(fn=="light"){
        line.textContent=sl+"You Have NowEntered Light Mode"
    }else if(fn=="dark"){
        line.textContent=sl+"You Have Now Entered Dark Mode"
    }else if(fn=="join"){
        join()
        line.textContent=sl+"Enter Event Name To Join"
    }

    if(fn!="error"){
        errNum=0
    }

    terminal.appendChild(line)
}


